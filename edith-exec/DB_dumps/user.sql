CREATE TABLE `user` (
    `id` BIGINT NOT NULL AUTO_INCREMENT,
    `email` VARCHAR(100) NOT NULL UNIQUE,
    `password` VARCHAR(100) NOT NULL,
    `vcs_base_url` VARCHAR(100) NOT NULL,
    `vcs_access_token` VARCHAR(100) NOT NULL,
    `created_date` TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    `last_modified_date` TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP NOT NULL,
    PRIMARY KEY (`id`)
);